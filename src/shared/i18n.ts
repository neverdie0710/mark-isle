type MessageKey = keyof typeof messages['zh-CN']
type Params = Record<string, string | number>

export function normalizeLocale(value?: string | null): Locale {
  return value === 'zh-CN' ? 'zh-CN' : 'en'
}

export function detectLocale(): Locale {
  const stored =
    typeof localStorage === 'undefined' ? null : localStorage.getItem(LOCALE_KEY)
  if (stored) return normalizeLocale(stored)
  return 'en'
}

export function tr(locale: Locale, key: MessageKey, params?: Params): string {
  const template = messages[locale][key] ?? messages['zh-CN'][key]
  if (!params) return template
  return template.replace(/\{(\w+)\}/g, (_, name: string) =>
    String(params[name] ?? `{${name}}`),
  )
}

export function localeLabel(locale: Locale): string {
  return messages[locale].languageName
}
